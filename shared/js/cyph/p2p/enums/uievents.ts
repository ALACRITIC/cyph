/**
 * P2P UI events.
 * 
 * Base events: connected, enable, videoToggle.
 * 
 * Request events: acceptConfirm, acceptConfirmation,
 * requestConfirm, requestConfirmation, requestRejection.
 */
export enum UIEvents {
	acceptConfirm,
	acceptConfirmation,
	connected,
	enable,
	requestConfirm,
	requestConfirmation,
	requestRejection,
	videoToggle
}
